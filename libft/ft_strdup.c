#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	int		i;
	size_t	len;

	len = ft_strlen(src);
	dest = malloc(sizeof(char) * (len + 1));
	i = 0;
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
